public class SomeCode {


    static int square(int x) {
        int squareOfX = x * x;
        return squareOfX;
    }

    static int addition(int x, int y) {
        int sum = x + y;
        return sum;
    }

    static int subtraction(int x, int y) {
        int difference = x - y;
        return difference;
    }

    static int division(int x, int y) {
        int quota = x / y;
        return quota;
    }
}
